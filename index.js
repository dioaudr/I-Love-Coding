//FOR
for(var opp = 1; opp < 20; opp++){
    console.log(opp + " - " + "I love coding");
}
for(var opp2 = 20; opp2 > 0; opp2--){
    console.log(opp2 + " - " + "I will become fullstack developer");
} 
//WHILE
var op2 = 1
while (op2 < 20){
    console.log(op2 + " - " + "I love coding")
    op2++
}
var op3 = 20
while (op3 > 0){
    console.log(op3 + " - " + "I will become fullstack developer")
    op3--
}